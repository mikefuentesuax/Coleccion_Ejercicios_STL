#include <cmath>
namespace matematicas {
    int suma(int a, int b) {
        return a + b;
    }
    int resta(int a, int b) {
        return a - b;
    }
    double division(double a, double b){
        return a/b;
    }
    int expo(int a){
        return a*a;
    }
    double raiz(double a, double b){
        return pow(a, 1/b);
    }
    double logaritmo(double a){
        return log(a);
    }

}


