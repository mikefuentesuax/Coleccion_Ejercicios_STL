#include<cmath>
namespace geometria{
    double triangulo(double a, double b){
        return (a*b)/2;
    }
    double circulo(double a){
        return M_PI*a*a;
    }
    double cuadrado(double a){
        return a*a;
    }
    double rectangulo(double a, double b){
        return a*b;
    }
}
    
